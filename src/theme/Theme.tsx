import { MD3LightTheme as DefaultTheme, MD3DarkTheme as DarkTheme } from 'react-native-paper'
import { useColorScheme, StatusBar } from 'react-native'

export const getCurrentTheme = () => {
  return isDark() ? darkTheme : lightTheme
}

export const isDark = () => {
  const scheme = useColorScheme()

  return scheme === 'dark'
}

export const setStatusBarColor = () => {
  StatusBar.setBarStyle(isDark() ? 'light-content' : 'dark-content')
  StatusBar.setBackgroundColor(isDark() ? darkTheme.colors.background : lightTheme.colors.background)
}

